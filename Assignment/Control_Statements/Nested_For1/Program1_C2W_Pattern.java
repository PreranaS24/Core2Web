// Program to print the below pattern.
/*
 * c2w c2w c2w
 * c2w c2w c2w
 * c2w c2w c2w
 */
class Program1_c2w_Pattern{

	public static void main(String[] args){

		for(int i = 0; i < 3; i++){

		 	for(int j = 0; j < 3; j++){

				System.out.print("c2w ");
			}
			
			System.out.println();
		}

    }
  }

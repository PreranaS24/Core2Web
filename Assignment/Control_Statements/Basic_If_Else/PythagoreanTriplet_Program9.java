//Program to check whether given numbers form PythagoreanTriplet or not.
//
//

class PythagoreanTriplet{

	public static void main(String[] args){

		int a = 2;//3;
		int b = 2;//4;
		int c = 2;//5;

		if (((a*a) + (b*b)) == (c*c))
			System.out.println("It's Pythagorean Triplet");
		else
			System.out.println("It is not a Pythagorean Triplet");



     }
   }

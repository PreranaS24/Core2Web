//Program to check given number is greater or lesser than 10
//


class GreaterLesser_Than_Ten{

	public static void main(String[] args){

	int var1 = 5;
        int var2 = 16;
	int var3 = 10;

	if (var1 > 10)
		System.out.println(var1 + " is greater than 10");
	else
		System.out.println(var1 + " is less than 10.");

	if (var2 > 10)
		System.out.println(var2 + " is greater than 10");
	else
		System.out.println(var2 + " is less than 10.");

	if (var3 > 10)
		System.out.println(var3 + " is greater than 10");
	else
		System.out.println(var3 + " is less than 10.");
    }
  }

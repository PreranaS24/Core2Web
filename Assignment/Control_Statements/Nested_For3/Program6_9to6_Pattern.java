/* 9
 * 9 8  
 * 9 8 7 
 * 9 8 7 6
 */

class Program6_9To6_Pattern{

	public static void main(String[] args){


	for(int i = 1 ; i <= 4 ; i++){

		int x = 9;

		for(int j = 1 ; j <= i ; j++){

			System.out.print(x + " " );
			x--;

		}

		System.out.println();
	}


          }
	}
